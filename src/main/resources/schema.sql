DROP TABLE IF EXISTS PERSON cascade;

CREATE TABLE PERSON ( ID INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, FNAME VARCHAR(100) NOT NULL,LNAME VARCHAR(100) NOT NULL ,
PHONE VARCHAR(13),EMAIL VARCHAR(25));


DROP TABLE IF EXISTS VACCINEINFO cascade;

CREATE TABLE VACCINEINFO ( ID INT GENERATED BY DEFAULT AS IDENTITY ,
foreign key (ID) references PERSON(ID)
 ,FDOSE VARCHAR(50) 
 ,SDOSE VARCHAR(50) 
  ,FDOSE_DATE DATE
 ,SDOSE_DATE DATE 
);

DROP TABLE IF EXISTS CITIZEN_INFO cascade;
CREATE TABLE CITIZEN_INFO ( NATIONAL_ID VARCHAR(12) PRIMARY KEY ,
 FNAME VARCHAR(100) NOT NULL,
 LNAME VARCHAR(100) NOT NULL ,
 NATIONAL_ID_TYPE VARCHAR(12)
 );


 DROP TABLE IF EXISTS CITIZEN_ZIPCODE cascade;
 CREATE TABLE CITIZEN_ZIPCODE (NATIONAL_ID VARCHAR(12), foreign key (NATIONAL_ID) references CITIZEN_INFO(NATIONAL_ID),
 ZIPCODE VARCHAR(7)
  ) ;

   DROP TABLE IF EXISTS VACCINE_CENTRE_INFO cascade;
 CREATE TABLE VACCINE_CENTRE_INFO (id INT GENERATED BY DEFAULT AS IDENTITY , 
 ZIPCODE VARCHAR(7),
 VACCINE_NAME VARCHAR(20),
 QUANITY_AVAILABLE INTEGER
  ) ;



   DROP TABLE IF EXISTS PERSON_HEALTH_INFO cascade;
 CREATE TABLE PERSON_HEALTH_INFO (NATIONAL_ID VARCHAR(12) , 
 age INTEGER,
 CHRONIC BOOLEAN,
 DIABETIC BOOLEAN
  ) ;

